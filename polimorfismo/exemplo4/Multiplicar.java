package polimorfismo.exemplo4;

public class Multiplicar {
    public int multiplica(int x, int y) {
        return x * y;
    }

    public int multiplica(int x, int y, int z) {
        return x * y * z;
    }

    public double multiplica(double x, double y) {
        return x * y;
    }
    public double multiplica(double x, double y, double z) {
        return x * y * z;
    }
}

